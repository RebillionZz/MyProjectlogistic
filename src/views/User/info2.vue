<h3>2. ສິນຄ້າຕ້ອງຫ້າມ</h3>
<p>ປະເພດສິນຄ້າທີ່ບໍ່ສາມາດຮັບບໍລິການໄດ້ ໄດ້ແກ່:</p>
<p>ປະເພດສິນຄ້າທີ່ຜິດກົດໝາຍ: ຢາເສບຕິດ, ຝິ່ນ, ເຮໂລອິນ, ກັນຊາ,
     ...</p>
     <p>2. ສິນຄ້າໜີພາສີທີ່ບໍ່ໄດ້ມີການເສຍພາສີອາກອນຖືກຕ້ອງຕາມກົດໝາຍ.</p>
     <p>3. ສິນຄ້າທີ່ເປັນອັນຕະລາຍ:​ອາວຸດເສິກສົງຄາມ, ອາວຸດທຸກປະເພດ, ວັດ
    ຖຸໄວໄຟ, ໝໍ່ໄຟທົ່ວໄປທີ່ຜ່ານການໃຊ້ງານມາແລ້ວ, ເຄື່ອງອີເລັກໂທນິກ
    ທີ່ຜ່ານການໃຊ້ງານມາແລ້ວ.</p>
    <p>4. ວັດຖຸທີມີແຮງດັນສູງຂອງແຫຼວສານເຄມີ: ບັ້ງແກັດ, ບັ້ງອົກຊີ, ສະເປ, 
    ບາຫຼອດ...</p>
    <p>5. ວັດຖຸມີຄ່າ:​ ເງິນ, ຄຳ, ເງິນສົດ, ພັນທະບັດ...</p>
    <p>6. ວັດຖຸທີ່ມີຄ່າທາງບູຮານເຊັ່ນ: ເຂົາກວາງ, ງາຊ້າງ, ເຄື່ອງປ່າຂອງດົງ...</p>
    <p>7. ເຄື່ອງເພັດ, ພອຍ, ທອງຄຳແທ່ງ ຫຼື ເງິນແທ່ງ ແລະ ອັນຍະມະນີອື່ນທີ່
    ມີມູນຄ້າສູງກວ່າ 500,000 ກີບ ( ຫ້າແສນກີບຖ້ວນ ).</p>
    <p>8. ພາສີອາກອນ ຫຼື ສະແຕມ.</p>
    <p>9. ຊິ້ນສ່ວນມະນຸດ, ກະດູກ ຫຼື ເທົ່າອະຖິ.</p>
    <p>10. ປະເພດສັດລ້ຽງທີ່ມີຊີວິດ:​ ໄກ່, ໝາ, ແມວ ແລະ ອື່ນໆ.</p>
    <p>11. ປະເພດອາຫານສົດທີ່ມີນ້ຳ, ໝັກດອງ, ອາຫານທະເລ, ໝາກໄມ້ທີ່ມີ
     ກິ່ນເໝັນ ແລະ ບູດ ເນົ່າງ່າຍ, ປາແດກ, ອາຫານທີ່ຕ້ອງຄວບຄຸມ
     ດ້ວຍອຸນກະພູມ...</p>
     <p>12. ເອກະສານທາງການເງິນ: ປະເພດບັດເອທີເອັມ (ATM) ບັດເດບິດ
     (Debit), ເຄຣດິດ (Credit), ແຊັກເງິນສົດ, ທະນາບັດ ຫຼື ບັດ
     ແທນເງິນສົດຕ່າງໆ. </p>
     <p>ເອກະສານສ່ວນບຸກຄົນທີ່ອອກໂດຍໜ່ວຍງານລັດຖະບານເຊັ່ນ: ບັດປະຈຳຕົວ, ໜັງສືເດີນທາງ (Passport), ໃບຂັບຂີ່, ສຳມະໂນ
     ຄົວ, ປ້າຍລົດ ຫຼື ທະບຽນລົດທຸກປະເພດ. ໃນກໍລະນີທີ່ລູກຄ້າຕ້ອງ
     ການຝາກ ລູກຄ້າຕ້ອງເຊັນເອກະສານຍິນຍອມໃນການຝາກສິນຄ້າ
     ພ້ອມນັ້ນທາງບໍລິສັດຂໍສະຫງວນສິດໃນການຊົດເຊີຍໃນວົງເງິນບໍ່ເກີນ
     500,000 ກີບ/ກ່ອງພັດສະດຸ ລູກຄ້າຕ້ອງເປັນຄົນດຳເນີນເອກະ
     ສານດ້ວຍຕົນເອງ ແລ້ວນຳໃບບິນຈ່າຍເງິນທີ່ກ່ຽວຂ້ອງກັບເອກະສານ
     ໂດຍກົງເທົ່ານັ້ນມາຍື່ນຮັບເງິນຄືນ ( ຍົກເວັ້ນເອກະສານທີ່ຈ່າຍພິເສດ
     “ເອົາດ່ວນ” ທາງບໍລິສັດຂໍສະຫງວນລິຂະສິດທີ່ຈະຊົດເຊີຍຄືນໃຫ້ ).
     ຫາກລູກຄ້າທ່ານໃດທີ່ຝ່າຝືນໃນການໃຊ້ບໍລິການຈັດສົ່ງສິນຄ້າຕາມ
     ບາງປະເພດທີ່ໄດ້ກຳນົດມາຂ້າງເທິງແມນບໍລິສັດຈະບໍ່ຮັບຜິດຊອບ
     ຕໍ່ທຸກຄວາມເສຍຫາຍໃດໆທັງໝົດ, ຫາກກໍລະນີທີ່ເຄື່ອງຂອງຕົນໄດ້
     ສ້າງຄວາມເສຍຫາຍໃຫ້ສິນຄ້າຂອງລູກຄ້າລາຍອື່ນແມ່ນລູກຄ້າຈະ
     ຕ້ອງໄດ້ຊົດໃຊ້ທຸກໆຄວາມເສຍຫາຍທີ່ອາດເກີດຂຶ້ນທັງໝົດລວມທັງ
     ຕົ້ນທຶນຂອງການຈັດສົ່ງສິນຄ້າໃນຄັ້ງນັ້ນໆດ້ວຍ.

     ໝາຍເຫດ: ສຳເນົາເອກະສານແມ່ນສາມາດຮັບຝາກໄດ້ (copy)</p>
