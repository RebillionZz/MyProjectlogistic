            <h3>1.1 ການຫຸ້ມຫໍ່ພັດສະດຸ</h3>
            <p>ການຫຸ້ມຫໍ່ຕ້ອງມີຄວາມແໜ້ນໜາ ບໍ່ວ່າຈະເປັນເຄື່ອງນ້ອຍ, ເຄື່ອງ
     ໃຫຍ່.</p>
     <p>ເຄື່ອງທີ່ແຕກຫັກງ່າຍ ຫຼື ສ່ຽງຕໍ່ການເປ່ເພ ຕ້ອງໃຫ້ລູກຄ້າຫຸ້ມຫໍ່ມາ
ຢ່າງດີມີແນວກັນກະແທກເພື່ອຫລຸດຜ່ອນຄວາມເສຍຫາຍລະຫວ່າງ
ການຂົນສົ່ງ.</p>
<p>ການແພັກສະກ໋ອດຄືນໃຫ້ແກ່ລູກຄ້າໃນກໍລະນີທີ່ສັງເກດເຫັນວ່າແກັດ
ເຄື່ອງມີຄວາມສ່ຽງຕໍ່ການຂາດ ຫຼື ຕົກເຮ່ຍໃນເວລາທີ່ຍົກຍໍ.</p>
<p>ການຫຸ້ມຫໍ່ເຄື່ອງຄືນໃໝ່ໃຫ່ແກ່ລູກຄ້າຕ້ອງມີການສ່ອງໜ້າກັບເຈົ້າ
ຂອງເຄື່ອງ ຫາກກໍລະນີທີ່ລູກຄ້າບໍ່ຢູ່ຕ້ອງໄດ້ໂທແຈ້ງພ້ອມທັງເກັບ
ຮູບໄວ້ເປັນຫຼັກຖານກ່ອນການຫໍ່ຄືນໃໝ່,ການໂທແຈ້ງຕ້ອງໄດ້ໂທແຈ້ງທັງລຸກຄ້າຕົ້ນທາງ ແລະ ລູກຄ້າປາຍທາງສະເໜີເພື່ອຈະໄດ້ຮັບຮູ້ກັນ
ທັງ 3 ຝ່າຍ, ເປັນການຫຼີກລ້ຽງບັນຫາທີ່ອາດຈະເກີດຂຶ້ນ.</p>
<p>ສິນຄ້າໃດທີ່ສາມາດໃສ່ຖົງກັນນ້ຳໄດ້ແມ່ນໃຫ້ບັນຈຸລົງໃສ່ໃນຖົງໃຫ້ໝົດ.</p>
